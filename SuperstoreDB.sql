--Listing the top Product by Sales showing OrderID, Order_Date, Product_Name, Ship_Date
SELECT TOP (20)
	OrderID,
	Order_Date,
	Product_Name,
	Ship_Date,
	SUM(Order_Quantity * Unit_Price) AS [Total Sales]
FROM Orders
GROUP BY
	OrderID, Order_Date, Product_Name, Ship_Date
ORDER BY
	[Total Sales] DESC;

--Analyzing the trends over time in months
SELECT
	DATEFROMPARTS(YEAR(Order_Date),MONTH(Order_Date), 1) AS [Month],
	COUNT(*) AS Order_count,
	SUM(Sales) AS [Monthly Sales],
	AVG(Sales) AS [Average Sales]
FROM Orders
GROUP BY
	DATEFROMPARTS(YEAR(Order_Date),MONTH(Order_Date), 1)
ORDER BY
	[Month];


--List of Product Returned showing Order_Date, Ship_Date, Customer_Name, Product_Name, Status

--Order_Date---------Orders-----O
--Ship_Date---------Orders------O
--Customer_Name-----Orders------O
--Product_Name------Orders------O
--Status------------Returns-----R
--Relatioship
--(OrderID and RowID)-------O and R

SELECT
	O.Order_Date,
	O.Ship_Date,
	O.Customer_Name,
	O.Product_Name,
	R.[Status]
FROM Orders O
JOIN
Returns R ON O.OrderID = R.OrderID
GROUP BY
	O.Order_Date,
	O.Ship_Date,
	O.Customer_Name,
	O.Product_Name,
	R.Status
ORDER BY
	R.Status DESC;


