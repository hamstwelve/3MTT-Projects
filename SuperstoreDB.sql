--Creating 3 Distinct Customer Segments Based on Revenue
WITH [Revenue Data] AS (
	SELECT Customer_Name, Region, Province, SUM(Order_Quantity * Unit_Price) AS Revenue,
		NTILE(3) OVER (ORDER BY SUM(Order_Quantity * Unit_Price)) AS Segment
			FROM Orders
			GROUP BY Customer_Name, Region, Province
	)
	SELECT
		Customer_Name, Region, Province, Revenue,
		CASE
			WHEN Segment = 1 THEN 'Low Revenue'
			WHEN Segment = 2 THEN 'Medium Revenue'
			WHEN Segment = 3 THEN 'High Revenue'
		END AS [Customer Segment]
		FROM [Revenue Data]
		ORDER BY Revenue DESC

